/** Returns niyyah options for a given activityId */
export function getNiyyahOptions(
  activityId: string,
  profileTags: string[] = []
): NiyyahOption[] {
  return NIYYAH_OPTIONS.filter(
    (n) =>
      n.activityId === activityId &&
      (!n.profileTag || profileTags.includes(n.profileTag))
  );
}
