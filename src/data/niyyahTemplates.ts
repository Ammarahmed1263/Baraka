export function getNiyyahOptions(
  activityId: string,
  profileTags: string[] = [],
): NiyyahOption[] {
  return NIYYAH_OPTIONS.filter(
    (n) =>
      n.activityId === activityId &&
      (!n.profileTags ||
        n.profileTags.length === 0 ||
        n.profileTags.some((tag) => profileTags.includes(tag))),
  );
}
